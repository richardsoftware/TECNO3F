#calcular Ley de Ohm corriente, voltaje y resistencia
print("Calcular Ley de Ohm")
corriente = float(input("Ingrese el valor de la corriente: "))
voltaje = float(input("Ingrese el valor del voltaje: "))

resistencia = voltaje / corriente

print(f"El valor de la resistencia es {resistencia} ohmios")